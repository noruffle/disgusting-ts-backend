import {Schema} from 'mongoose';
import {ICat} from '../interface/cat.interface';

export const schema = new Schema<ICat>(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
)